function showsignup() {
  var i = document.getElementsByClassName('main_content');
  var x = document.getElementsByClassName('signup');
  i[0].style.display = 'none';
  x[0].style.display = 'block';
}

function showlogin() {
  var i = document.getElementsByClassName('main_content');
  var x = document.getElementsByClassName('log_in');
  i[0].style.display = 'none';
  x[0].style.display = 'block';
}
